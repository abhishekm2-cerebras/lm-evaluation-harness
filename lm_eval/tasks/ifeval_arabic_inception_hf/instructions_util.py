def split_into_sentences(text: str) -> List[str]:
    """
    Splits text into sentences. This version is adapted for both English and Arabic.
    It splits based on common sentence-ending punctuation.
    Note: This is a simplified approach. For highly accurate Arabic sentence
    tokenization, a dedicated library like CAMeL Tools would be superior.
    """
    # Split by periods, question marks (English and Arabic), and exclamation marks.
    # We use a positive lookbehind `(?<=[...])` to keep the delimiter at the end of the sentence.
    sentences = re.split(r'(?<=[.?!؟])\s+', text)
    return [s.strip() for s in sentences if s.strip()]


def count_words(text: str) -> int:
    """
    Counts the number of words in a string, supporting Arabic and English.
    A "word" is defined as a sequence of alphanumeric characters, including Arabic letters.
    """
    # \u0600-\u06FF is the Unicode range for Arabic characters.
    # This pattern finds sequences of English letters, numbers, or Arabic letters.
    words = re.findall(r'[\w\u0600-\u06FF]+', text)
    return len(words)


def count_sentences(text: str) -> int:
    """
    Counts the number of sentences in a string using our adapted splitter.
    """
    return len(split_into_sentences(text))


